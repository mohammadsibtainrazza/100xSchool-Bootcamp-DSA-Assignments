#include <iostream>
using namespace std;

int main(){
    int n;
    long long m;
    cin>>m>>n;
    cout<<m<<" + "<<n<<" = "<<m+n<<endl<<endl;
    cout<<m<<" - "<<n<<" = "<<m-n<<endl<<endl;
    cout<<m<<" * "<<n<<" = "<<m*n<<endl<<endl;
    cout<<m<<" / "<<n<<" = "<<m/n<<endl<<endl;
    cout<<m<<" % "<<n<<" = "<<m%n<<endl<<endl;
}